<p>9. <strong> Automatizált döntéshozatal egyedi ügyekben, beleértve a profilalkotást </strong></p>
<p>&nbsp;</p>
<p>Ön jogosult arra, hogy ne terjedjen ki Önre az olyan, kizárólag automatizált adatkezelésen – ideértve a profilalkotást is – alapuló döntés hatálya, amely rá nézve joghatással járna vagy őt hasonlóképpen jelentős mértékben érintené.</p>
<p>Az előző bekezdés nem alkalmazandó abban az esetben, ha a döntés:</p>
<p>· Ön és az adatkezelő közötti szerződés megkötése vagy teljesítése érdekében szükséges;</p>
<p>· meghozatalát az adatkezelőre alkalmazandó olyan uniós vagy tagállami jog teszi lehetővé, amely Ön jogainak és szabadságainak, valamint jogos érdekeinek védelmét szolgáló megfelelő intézkedéseket is megállapít; vagy</p>
<p>· Ön kifejezett hozzájárulásán alapul.</p>
<p>Intézkedési határidő</p>
<p>Az adatkezelő indokolatlan késedelem nélkül, de mindenféleképpen a kérelem beérkezésétől számított <strong>1 hónapon belül</strong> tájékoztatja Önt a fenti kérelmek nyomán hozott intézkedésekről.</p>
<p>Szükség esetén ez <strong>2 hónappal meghosszabbítható</strong>. A határidő meghosszabbításáról az adatkezelő a késedelem okainak megjelölésével a kérelem kézhezvételétől számított <strong>1 hónapon belül</strong> tájékoztatja Önt.</p>
<p>Ha az adatkezelő nem tesz intézkedéseket Ön kérelme nyomán, <strong> késedelem nélkül, de legkésőbb a kérelem beérkezésétől számított egy hónapon belül tájékoztatja Önt az intézkedés elmaradásának okairól </strong> , valamint arról, hogy Ön panaszt nyújthat be valamely felügyeleti hatóságnál, és élhet bírósági jogorvoslati jogával.</p>
<p>Az adatkezelés biztonsága</p>
<p>Az adatkezelő és az adatfeldolgozó a tudomány és technológia állása és a megvalósítás költségei, továbbá az adatkezelés jellege, hatóköre, körülményei és céljai, valamint a természetes személyek jogaira és szabadságaira jelentett, változó valószínűségű és súlyosságú kockázat figyelembevételével megfelelő technikai és szervezési intézkedéseket hajt végre annak érdekében, hogy a kockázat mértékének megfelelő szintű adatbiztonságot garantálja, ideértve, többek között, adott esetben:</p>
<p>a) a személyes adatok álnevesítését és titkosítását;</p>
<p>b) a személyes adatok kezelésére használt rendszerek és szolgáltatások folyamatos bizalmas jellegének biztosítását, integritását, rendelkezésre állását és ellenálló képességét;</p>
<p>c) fizikai vagy műszaki incidens esetén az arra való képességet, hogy a személyes adatokhoz való hozzáférést és az adatok rendelkezésre állását kellő időben vissza lehet állítani;</p>
<p>d) az adatkezelés biztonságának garantálására hozott technikai és szervezési intézkedések hatékonyságának rendszeres tesztelésére, felmérésére és értékelésére szolgáló eljárást.</p>
<p>Az érintett tájékoztatása az adatvédelmi incidensről</p>
<p>Ha az adatvédelmi incidens valószínűsíthetően magas kockázattal jár a természetes személyek jogaira és szabadságaira nézve, az adatkezelő indokolatlan késedelem nélkül tájékoztatja az érintettet az adatvédelmi incidensről.</p>
<p>Az érintett részére adott tájékoztatásban <strong>világosan és közérthetően</strong> ismertetni kell az adatvédelmi incidens jellegét, és közölni kell az adatvédelmi tisztviselő vagy a további tájékoztatást nyújtó egyéb kapcsolattartó nevét és elérhetőségeit; ismertetni kell az adatvédelmi incidensből eredő, valószínűsíthető következményeket; ismertetni kell az adatkezelő által az adatvédelmi incidens orvoslására tett vagy tervezett intézkedéseket, beleértve adott esetben az adatvédelmi incidensből eredő esetleges hátrányos következmények enyhítését célzó intézkedéseket.</p>
<p>Az érintettet nem kell tájékoztatni, ha a következő feltételek bármelyike teljesül:</p>
<p>· az adatkezelő <strong> megfelelő technikai és szervezési védelmi intézkedéseket hajtott végre </strong> , és ezeket az intézkedéseket az adatvédelmi incidens által érintett adatok tekintetében alkalmazták, különösen azokat az intézkedéseket – mint például a titkosítás alkalmazása –, amelyek a személyes adatokhoz való hozzáférésre fel nem jogosított személyek számára <strong>értelmezhetetlenné teszik az adatokat;</strong></p>
<p>· az adatkezelő az adatvédelmi incidenst követően olyan további intézkedéseket tett, amelyek <strong> biztosítják, hogy az érintett jogaira és szabadságaira jelentett, magas kockázat a továbbiakban valószínűsíthetően nem valósul meg </strong> ;</p>
<p>· a tájékoztatás <strong>aránytalan erőfeszítést tenne szükségessé</strong> . Ilyen esetekben az érintetteket nyilvánosan közzétett információk útján kell tájékoztatni, vagy olyan hasonló intézkedést kell hozni, amely biztosítja az érintettek hasonlóan hatékony tájékoztatását.</p>
<p>Ha az adatkezelő még nem értesítette az érintettet az adatvédelmi incidensről, a felügyeleti hatóság, miután mérlegelte, hogy az adatvédelmi incidens valószínűsíthetően magas kockázattal jár-e, elrendelheti az érintett tájékoztatását.</p>
